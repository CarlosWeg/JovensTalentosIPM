--ATIVIDADE 4

--a) 

SELECT *
  FROM TREINA.TBPESSOA
 WHERE PESSEXO = 2
 ORDER BY PESNOME ASC
 LIMIT 2
OFFSET 2;

--b)

SELECT PESCODIGO AS "Código da Pessoa",
       IMVCODIGO AS "Código do Imóvel",
	   TAXCODIGO AS "Código da Taxa",
       TAXVALOR AS "Valor da Taxa"
  FROM TREINA.TBTAXA
 WHERE TAXVALOR > 80
 ORDER BY TAXVALOR DESC
OFFSET 1;

